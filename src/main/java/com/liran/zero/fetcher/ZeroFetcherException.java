package com.liran.zero.fetcher;

public class ZeroFetcherException extends Exception {

	private static final long serialVersionUID = -2262317550630974758L;

	public ZeroFetcherException() {
		super();
	}

	public ZeroFetcherException(String msg) {
		super(msg);
	}

	public ZeroFetcherException(Throwable cause) {
		super(cause);
	}

	public ZeroFetcherException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
